package com.jsacristan.word_card_back.exceptions;

/**
 * Enumeration of base exception types.
 * This enumeration defines common types of exceptions used in the application.
 */
public enum BaseExceptionEnum {

    /**
     * Exception indicating that the requested resource was not found.
     */
    NOT_FOUND,

    /**
     * Exception indicating an error occurred while saving data.
     */
    ERROR_SAVING,

    /**
     * Exception indicating that no content is available.
     */
    NO_CONTENT
}
